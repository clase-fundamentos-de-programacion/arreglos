package com.mycompany.arreglos;

public class DefinicionEInicializacion {
    public static void main(String[] args) {
        // Definición
        int numero;
        // Inicialización (Asignación de memoria)
        numero = 10;
        
        // Definición
        int[] numeros;
        // Inicialización (Asignación de memoria)
        numeros = new int[4];
        
        numeros[0] = 15;
        numeros[1] = 10;
        numeros[2] = 3;
        numeros[3] = 8;
    }
}
